#include <string.h>
/**
 *_strlen- recives an argument to return the len of a string
 *
 * @s: The string whose length is to be calculatedCounts the length of a string
 * Return: returns return len of a string to the main.
 */
int _strlen(char *s) /* _strlen - Counts the length of a string*/
{
	int count;

	count = strlen(s);
	return (count);
}
